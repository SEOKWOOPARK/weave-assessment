def hex_to_rgba(hex_color: str, alpha: float) -> str:
    """Convert a 6-digit hex color + float alpha (0–1) to an rgba() string.

    Plotly color properties accept rgba() but NOT 8-digit hex (#RRGGBBAA).
    Always use this helper instead of string-concatenating opacity suffixes.

    Example:
        hex_to_rgba("#F54E00", 0.53)  →  "rgba(245, 78, 0, 0.53)"
    """
    hex_color = hex_color.lstrip("#")
    r, g, b = (int(hex_color[i:i+2], 16) for i in (0, 2, 4))
    return f"rgba({r}, {g}, {b}, {alpha})"


