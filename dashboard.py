@st.cache_data(show_spinner=False)
def load_data() -> dict:
    path = Path("posthog_data.json")
    if path.exists():
        with open(path) as f:
            raw = json.load(f)
        return raw
    return SAMPLE_DATA


def engineers_df(data: dict) -> pd.DataFrame:
    rows = []
    for eng in data["engineers"].values():
        rows.append({
            "login":             eng["login"],
            "total_score":       eng["total_score"],
            "pr_score":          eng["pr_score"],
            "review_score":      eng["review_score"],
            "merged_prs":        eng["merged_prs"],
            "reviews_given":     eng["reviews_given"],
            "approvals_given":   eng["approvals_given"],
            "changes_requested": eng["changes_requested"],
            "total_additions":   eng["total_additions"],
            "total_deletions":   eng["total_deletions"],
            "avg_response_hours": eng.get("avg_response_hours"),
            "labels_touched":    eng.get("labels_touched", []),
        })
    df = pd.DataFrame(rows).sort_values("total_score", ascending=False).reset_index(drop=True)
    df.index += 1
    return df


def tag_color(label: str) -> str:
    l = label.lower()
    if "bug" in l:      return "bug"
    if "enterprise" in l: return "enterprise"
    if "perf" in l:     return "perf"
    return ""


